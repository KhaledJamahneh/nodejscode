// lib/core/constants/app_radius.dart
/// Standardized border radius values for consistent UI
class AppRadius {
  // Prevent instantiation
  AppRadius._();

  /// Small radius: 8px (chips, badges)
  static const double sm = 8.0;

  /// Medium radius: 12px (buttons, inputs)
  static const double md = 12.0;

  /// Large radius: 16px (cards)
  static const double lg = 16.0;

  /// Extra large radius: 20px (modals, sheets)
  static const double xl = 20.0;

  /// Round radius: 999px (avatars, pills)
  static const double round = 999.0;
}
